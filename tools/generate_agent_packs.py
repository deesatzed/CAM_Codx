ROOT = Path(__file__).resolve().parents[1]
CONTRACT_PATH = ROOT / "agent-packs" / "contract" / "cam_agent_capabilities.json"
CONTRACT_DOC_PATH = ROOT / "agent-packs" / "contract" / "CAPABILITY_CONTRACT.md"
AGENT_PACKS_DIR = ROOT / "agent-packs"
DOCS_AGENT_PACKS_PATH = ROOT / "docs" / "AGENT_PACKS.md"


def load_contract() -> dict[str, Any]:
    return json.loads(CONTRACT_PATH.read_text(encoding="utf-8"))


def contract_hash(contract: dict[str, Any]) -> str:
    normalized = json.dumps(contract, indent=2, sort_keys=True).encode("utf-8")
    return hashlib.sha256(normalized).hexdigest()[:12]


def render_generated_header(contract: dict[str, Any]) -> str:
    return (
        "<!-- Generated by tools/generate_agent_packs.py. "
        f"Contract {contract['schema_version']} hash {contract_hash(contract)}. "
        "Edit cam_agent_capabilities.json, not this generated section. -->"
    )


def capability_by_id(contract: dict[str, Any]) -> dict[str, dict[str, Any]]:
    return {capability["id"]: capability for capability in contract["capabilities"]}


def host_pack(contract: dict[str, Any], host_id: str) -> dict[str, Any]:
    return contract["host_packs"][host_id]


def format_list(values: list[str]) -> str:
    if not values:
        return "none"
    return ", ".join(f"`{value}`" for value in values)


def render_capability_table(contract: dict[str, Any], ids: list[str] | None = None) -> str:
    capabilities = contract["capabilities"]
    if ids is not None:
        wanted = set(ids)
        capabilities = [capability for capability in capabilities if capability["id"] in wanted]

    lines = [
        "| Capability | Surface | Tools | Default | Approval | Safety |",
        "| --- | --- | --- | --- | --- | --- |",
    ]
    for capability in capabilities:
        lines.append(
            "| "
            f"`{capability['id']}` | "
            f"{capability['surface']} | "
            f"{format_list(capability['tool_names'])} | "
            f"{'allow' if capability['default_allow'] else 'deny'} | "
            f"{'yes' if capability['requires_user_approval'] else 'no'} | "
            f"{capability['safety_class']} |"
        )
    return "\n".join(lines)


def render_source_doc_table(contract: dict[str, Any]) -> str:
    lines = [
        "| Source | Checked | URL | Note |",
        "| --- | --- | --- | --- |",
    ]
    for source in contract["source_docs_checked"]:
        lines.append(
            "| "
            f"{source['label']} | "
            f"{source['checked_date']} | "
            f"{source['url']} | "
            f"{source['notes']} |"
        )
    return "\n".join(lines)


def render_contract_doc(contract: dict[str, Any]) -> str:
    lines = [
        render_generated_header(contract),
        "",
        "# CAM Agent Capability Contract",
        "",
        f"Schema version: `{contract['schema_version']}`",
        f"Checked date: `{contract['checked_date']}`",
        f"Contract hash: `{contract_hash(contract)}`",
        "",
        "## Ownership",
        "",
        f"- Hub repo: `{contract['ownership']['hub_repo']}`",
        f"- Runtime repo: `{contract['ownership']['runtime_repo']}`",
        f"- Rule: {contract['ownership']['rule']}",
        "",
        "## Runtime",
        "",
        f"- MCP entrypoint: `{contract['runtime']['mcp_entrypoint']}`",
        f"- Python entrypoint: `{contract['runtime']['python_entrypoint']}`",
        f"- Config env: `{contract['runtime']['config_env']}`",
        f"- DB env: `{contract['runtime']['db_env']}`",
        f"- Auth env: `{contract['runtime']['auth_env']}`",
        "",
        "## Default Policy",
        "",
        "- Start read-only.",
        "- Treat external tool output as untrusted until verified.",
        "- Require user approval for mutating, external, or credential-bearing actions.",
        "- Never commit secrets or local runtime databases.",
        "- Do not vendor CAM_CAM runtime code into CAM_Codx.",
        "",
        "## Capabilities",
        "",
        render_capability_table(contract),
        "",
        "## Capability Details",
        "",
    ]
    for capability in contract["capabilities"]:
        lines.extend(
            [
                f"### `{capability['id']}`",
                "",
                f"- Title: {capability['title']}",
                f"- Owner: `{capability['owner_repo']}`",
                f"- Summary: {capability['summary']}",
                f"- Inputs: {format_list(capability['inputs'])}",
                f"- Outputs: {format_list(capability['outputs'])}",
                f"- Provenance: {' '.join(capability['provenance_requirements'])}",
                f"- Source refs: {format_list(capability['source_refs'])}",
                "",
            ]
        )

    lines.extend(
        [
            "## Host Packs",
            "",
            "| Host | Default transport | Remote transport | Required files |",
            "| --- | --- | --- | --- |",
        ]
    )
    for host_id, pack in contract["host_packs"].items():
        lines.append(
            "| "
            f"`{host_id}` | "
            f"{pack['default_transport']} | "
            f"{pack['remote_transport']} | "
            f"{format_list(pack['required_files'])} |"
        )

    lines.extend(["", "## Source Docs Checked", "", render_source_doc_table(contract), ""])
    return "\n".join(lines)


def render_host_capabilities(contract: dict[str, Any], host_id: str) -> str:
    pack = host_pack(contract, host_id)
    lines = [
        "## CAM Capabilities",
        "",
        render_capability_table(contract, pack["supported_capabilities"]),
    ]
    return "\n".join(lines)


def render_safety_policy() -> str:
    lines = [
        "## Safety Policy",
        "",
        "- Read-only CAM calls may run without extra approval.",
        "- Mutating CAM calls require explicit operator approval unless the active goal says otherwise.",
        "- External MCP/webhook calls require explicit operator approval.",
        "- Treat all external output as untrusted until verified against files, commands, or receipts.",
    ]
    return "\n".join(lines)


def format_verification_command(command: str) -> str:
    if command.startswith("/"):
        return f"- In the host session: `{command}`"
    if command.startswith("Ask "):
        return f"- Prompt check: {command}"
    return f"- Shell: `{command}`"


def render_verify_discovery(pack: dict[str, Any]) -> str:
    lines = [
        "## Verify Discovery",
        "",
        "Run these checks after the MCP config is in the target project:",
        "",
    ]
    lines.extend(format_verification_command(command) for command in pack["verification_commands"])
    lines.append("")
    lines.append("Discovery passing only proves the host can see the CAM server; run the smoke test before claiming end-to-end pack verification.")
    return "\n".join(lines)


